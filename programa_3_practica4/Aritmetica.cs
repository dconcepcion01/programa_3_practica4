using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace programa_3_practica4

{   //clase Aritmetica
    class Aritmetica
    {
        //Metodo operacion suma
        public static int sumar(int numero1, int numero2)
        {
            return numero1 + numero2;

        }
        //Metodo operacion multiplicacion
        public static int Multi(int numero1, int numero2)
        {
            return numero1 * numero2;

        }
        //Metodo operacion resta
        public static int resta(int numero1, int numero2)
        {
            return numero1 - numero2;
        }
        //Metodo operacion division
        public static int division(int numero1, int numero2)
        {
            return numero1 / numero2;

        }
    }
}
